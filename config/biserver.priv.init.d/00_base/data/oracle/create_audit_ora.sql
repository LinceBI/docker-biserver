CREATE USER audit_user IDENTIFIED BY "audit_password" QUOTA UNLIMITED ON USERS;
GRANT CREATE SESSION, CREATE PROCEDURE, CREATE TABLE, CREATE TRIGGER, CREATE SEQUENCE TO audit_user;

CONNECT audit_user/audit_password;

CREATE TABLE AUDIT (
	JOB_ID VARCHAR2(200) NULL,
	INST_ID VARCHAR2(200) NULL,
	OBJ_ID VARCHAR2(200) NULL,
	OBJ_TYPE VARCHAR2(200) NULL,
	ACTOR VARCHAR2(200) NULL,
	MESSAGE_TYPE VARCHAR2(200) NULL,
	MESSAGE_NAME VARCHAR2(200) NULL,
	MESSAGE_TEXT_VALUE VARCHAR2(1024) NULL,
	MESSAGE_NUM_VALUE NUMBER(19,2) NULL,
	DURATION NUMBER(19,2) NULL,
	AUDIT_TIME TIMESTAMP NULL
);

COMMIT;
