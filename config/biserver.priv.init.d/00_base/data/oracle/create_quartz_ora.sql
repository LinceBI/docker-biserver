);

CREATE TABLE QRTZ5_TRIGGER_LISTENERS (
	TRIGGER_NAME VARCHAR2(200) NOT NULL,
	TRIGGER_GROUP VARCHAR2(200) NOT NULL,
	TRIGGER_LISTENER VARCHAR2(200) NOT NULL,
